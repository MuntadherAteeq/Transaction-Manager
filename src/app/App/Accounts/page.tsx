"use client";

import { AgGridReact } from "ag-grid-react";
import { useMemo, useState } from "react";

import {
  AllCommunityModule,
  ClientSideRowModelModule,
  ColDef,
  ModuleRegistry,
  NumberFilterModule,
  RowSelectionOptions,
  ValidationModule,
} from "ag-grid-community";
import { useTableTheme } from "@/hooks/use-TableTheme";
import { Badge } from "@/components/ui/badge";
import { Edit, Plus, Trash, User2 } from "lucide-react";
import { AvatarImage } from "@radix-ui/react-avatar";
import { useIsMobile } from "@/hooks/use-mobile";
import { Button } from "@/components/ui/button";
import { Card, CardContent } from "@/components/ui/card";

import { AddAccount } from "./Account-Dialog";
import { Account } from "@prisma/client";
import { Avatar, AvatarFallback } from "@/components/ui/avatar";
import useSWR from "swr";
import { Alert_Dialog } from "@/components/Alert_Dialog";

// Register the required modules
ModuleRegistry.registerModules([
  AllCommunityModule,
  ClientSideRowModelModule,
  NumberFilterModule,
  ValidationModule /* Development Only */,
]);

export default function AccountTable() {
  const tableTheme = useTableTheme();

  const isMobile = useIsMobile();

  const [rowData, setRowData] = useState<Account[]>([]);

  const { data, error, mutate, isLoading } = useSWR("/api/accounts?limit=10", {
    fetcher: (url: string) => fetch(url).then((res) => res.json()),
  });

  useMemo(() => {
    if (data) {
      setRowData(data);
    }
  }, [data]);

  const colDefs: ColDef[] = [
    {
      field: "image",
      headerName: "",
      width: 80,
      resizable: false,
      cellClass: "w-full h-full",
      filter: false,
      sortable: true,
      lockPosition: true,
      cellRenderer: (params: { value: string; data: Account }) => {
        return (
          <div className="flex items-center justify-center w-full h-full">
            <Avatar className="size-10">
              <AvatarImage
                className="size-10 shrink-0 rounded-full object-cover"
                src={params.value}
                alt="Image"
              />
              <AvatarFallback>{params.data.name.charAt(0)}</AvatarFallback>
            </Avatar>
          </div>
        );
      },
    },
    {
      field: "name",
      sortable: true,
      lockPosition: true,
      flex: isMobile ? 0 : 1,
      filter: true,
      floatingFilter: true,
    },
    {
      field: "email",
      sortable: true,
      lockPosition: true,
      flex: isMobile ? 0 : 1,
      filter: true,
      floatingFilter: true,
    },
    {
      field: "role",
      sortable: true,
      lockPosition: true,
      flex: isMobile ? 0 : 1,
      filter: true,
      floatingFilter: true,
      cellRenderer: (params: { value: string }) => {
        return (
          <div className="flex w-full h-full items-center justify-center gap-2 flex-wrap max-sm:overflow-y-scroll max-sm:overflow-x-hidden ">
            <Badge
              key={params.value}
              variant={params.value === "Admin" ? "destructive" : "default"}
            >
              <User2 className="mr-2 h-4 w-4 " />
              {params.value}
            </Badge>
          </div>
        );
      },
    },
  ];

  const rowSelection = useMemo<
    RowSelectionOptions | "single" | "multiple"
  >(() => {
    return {
      mode: "singleRow",
    };
  }, []);

  return (
    <div className=" flex flex-col w-full h-full">
      {/* Table Header */}
      <Card className="flex flex-row p-0 m-0 rounded-none">
        <CardContent className="w-full p-3 space-x-3">
          <AddAccount mutate={mutate}>
            <Button
              variant="default"
              // className="bg-transparent hover:bg-background border-2"
              aria-label="Add New Account"
            >
              <Plus />
              <span className="max-sm:hidden me-2">New</span>
            </Button>
          </AddAccount>
          <Button
            variant={"ghost"}
            className=" bg-transparent hover:bg-background  border-2 "
          >
            <Edit />
            <span className="max-sm:hidden me-2 ">Edit</span>
          </Button>

          <Alert_Dialog
            title={"Are You Sure ?"}
            variant="destructive"
            description={
              "This action will remove the user and cannot be undone. "
            }
            confirmText={"Delete"}
          >
            <Button
              variant={"destructive"}
              className=" bg-transparent hover:bg-background hover:text-destructive-foreground border-2"
            >
              <Trash />
              <span className=" max-sm:hidden me-2 ">Delete</span>
            </Button>
          </Alert_Dialog>
        </CardContent>
      </Card>
      <AgGridReact
        theme={tableTheme}
        className=" h-full w-full "
        rowData={rowData}
        columnDefs={colDefs}
        // this will make the grid responsive
        rowHeight={60}
        // This will prevent the column from removed when dragged out
        suppressDragLeaveHidesColumns
        // this will allow us to select multiple rows
        rowSelection={rowSelection}
      />
    </div>
  );
}
